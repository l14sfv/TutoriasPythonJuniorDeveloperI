class CuentaBancaria:
    def __init__(self, titular, saldo, tipo_cuenta):
        self.__titular = titular  #PRIVADO con Encapsulamiento __ para indicar que es privado
        self.__saldo = saldo
        self.__tipo_cuenta = tipo_cuenta

    def consultar(self):
            print(f"El saldo de la cuenta de {self.__titular} ({self.__tipo_cuenta}) es: {self.__saldo}")
            return self.__saldo 
            
        
    def depositar(self, cantidad):
            if cantidad > 0:
                self.__saldo += cantidad
                print(f"Depósito exitoso. Nuevo saldo: {self.__saldo}")
            else:
                print("Cantidad inválida. El depósito debe ser mayor a cero.")
                
    def retirar(self, cantidad):
            if cantidad > 0 and cantidad <= self.__saldo:
                self.__saldo -= cantidad
                print(f"Retiro exitoso. Nuevo saldo: {self.__saldo}")
            else:
                print("Cantidad inválida o saldo insuficiente para el retiro.")
                
    def eliminar_cuenta(self):
            self.__titular = None
            self.__saldo = 0
            print("Cuenta eliminada.")
            
